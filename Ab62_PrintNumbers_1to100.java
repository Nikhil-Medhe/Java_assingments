package ass;

public class Ab62_PrintNumbers_1to100 {
    public static void main(String[] args) {
        int number = 1; // Initialize the number

        // Using a while loop to print numbers from 1 to 100
        while (number <= 100) {
            System.out.println(number); // Print the current number
            number++; // Increment the number by 1
        }
    }
}