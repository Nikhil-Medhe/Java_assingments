package ass;

public class Ab61_WhileLoopExample {
    public static void main(String[] args) {
        int number = 1; // Initialize the number

        // Using a while loop to print numbers from 1 to 10
        while (number <= 10) {
            System.out.println(number); // Print the current number
            number++; // Increment the number by 1
        }
    }
}
