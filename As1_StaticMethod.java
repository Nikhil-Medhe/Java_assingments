package ass;

	
public class As1_StaticMethod {

	    // Static method
	    public static void displayMessage() {
	        System.out.println("This is a static method call.");
	    }

	    // Main method
	    public static void main(String[] args) {
	        // Calling the static method 5 times without a loop
	        displayMessage();
	        displayMessage();
	        displayMessage();
	        displayMessage();
	        displayMessage();
	    }
	}

